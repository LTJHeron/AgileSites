package wcs.java;

public class CSElement {
	
	public CSElement(String name, String element) {
		super();
		this.name = name;
		this.element = element;
	}

	private String name;
	
	private String element;

	public String getName() {
		return name;
	}

	public String getElement() {
		return element;
	}	
}
